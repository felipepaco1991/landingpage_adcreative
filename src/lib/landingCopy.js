const normalizeLang = (rawLang) => {
  const value = (rawLang || "pt").toLowerCase();
  if (value.startsWith("en")) {
    return "en";
  }
  return "pt";
};

export const getLang = () => normalizeLang(import.meta.env.VITE_LANG);

export const getCopy = () => landingCopy[getLang()];
